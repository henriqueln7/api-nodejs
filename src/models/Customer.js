const moongose = require('mongoose');

const CustomerSchema = moongose.Schema({
  name: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
  },
  password: {
    type: String,
    required: true,
  },
});

module.exports = moongose.model('Customer', CustomerSchema);
